// El substring() método de Stringvalores devuelve la parte de esta cadena desde el índice inicial hasta el índice final y excluyendolo, o hasta el final de la cadena si no se proporciona un índice final.

const str = 'Mozilla';

console.log(str.substring(1, 3));
// Expected output: "oz"

console.log(str.substring(2));
// Expected output: "zilla"
